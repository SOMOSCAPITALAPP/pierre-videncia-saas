export function shuffleDeck(deck: TarotCard[]) {
  const copy = [...deck];

  for (let i = copy.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [copy[i], copy[j]] = [copy[j], copy[i]];
  }

  return copy;
}

export function getCrossReading(deck: TarotCard[], userNumber: number) {
  const cards: TarotCard[] = [];

  for (let i = 1; i <= 5; i += 1) {
    const position = userNumber * i;
    const index = (position - 1) % deck.length;
    cards.push(deck[index]);
  }

  return cards;
}

